public class ForLoop {
 
    public static void main(String[] args) {
        
        System.out.println();

        for(int num =4;num > 0;num--){  // t t
            System.out.println("hi : "+num); //hi : 10

            for(int num1 =9;num1 > 0;num1--){ // t  10>10
                System.out.print(num1 +" ");// bye 10 
    
                    
            }  
            System.out.println();     
        }
    }
}


